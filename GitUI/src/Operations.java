    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String status = "";
        try{
            ProcessBuilder builder = new ProcessBuilder("cmd.exe", "/c", "cd "+Helper.repo+" && git status");
            builder.redirectErrorStream(true);
            Process p = builder.start();
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            while(true){
                String line = r.readLine();
                status = status + line + "\n";
                if(line == null) break;
            }
            JOptionPane.showMessageDialog(this, status);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Something's wrong");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String commit_message = JOptionPane.showInputDialog(this, "Input Commit Message!");
        try{
            ProcessBuilder builder = new ProcessBuilder("cmd.exe", "/c", "cd "+Helper.repo+" && git commit -m \""+commit_message+"\"");
            builder.redirectErrorStream(true);
            Process p = builder.start();
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            while(true){
                String line = r.readLine();
                System.out.println(line);
                if(line == null) break;
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Something's wrong");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String remote = JOptionPane.showInputDialog(this, "Input Remote");
        String branch = JOptionPane.showInputDialog(this, "Input branch");
        try{
            ProcessBuilder builder = new ProcessBuilder("cmd.exe", "/c", "cd "+Helper.repo+" && git push "+remote+" "+branch);
            builder.redirectErrorStream(true);
            Process p = builder.start();
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            while(true){
                String line = r.readLine();
                System.out.println(line);
                if(line == null) break;
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Something's wrong");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

