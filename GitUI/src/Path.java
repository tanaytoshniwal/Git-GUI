    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Helper.path = input_path.getText().toString().trim();
        if(Helper.path.equals("")){
            JOptionPane.showMessageDialog(this, "Invalid input!");
        }
        else{
            if(checkURL(Helper.path)){
                Helper.repo = repo_name.getText().toString().trim();
                try{
                    ProcessBuilder builder = new ProcessBuilder("cmd.exe", "/c", "git clone "+Helper.path+" && cd "+Helper.repo);
                    builder.redirectErrorStream(true);
                    Process p = builder.start();
                    BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
                    String line = "";
                    while(true){
                        line = r.readLine();
                        if(line == null) break;
                        System.out.println(line);
                    }
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(this, "There's something wrong!");
                }
                new Operations().setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

