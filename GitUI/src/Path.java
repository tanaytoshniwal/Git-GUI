    public boolean checkURL(String url){
        try{
            new URL(url).toURI();
            if(url.substring(0, 18).equals("https://github.com"))
                return true;
            else
                return false;
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Invalid input!");
            return false;
        }
    }
    
